import path from "node:path";
import type { SpecModel, Schema } from "../models.js";
import { cppConstName, cppName, hex, sortById, writeTextFile } from "../util.js";

const banner = `// Generated by axtp-gen. Do not edit manually.\n`;

function enumBlock(name: string, type: string, items: { name: string; id: number }[]): string {
  const width = type.includes("uint8") ? 2 : 4;
  const rows = sortById(items).map((item) => `    ${cppName(item.name)} = ${hex(item.id, width)},`).join("\n");
  return `enum class ${name} : ${type} {\n${rows}\n};`;
}

function fieldType(type: string): string {
  const map: Record<string, string> = {
    uint8: "FieldType::Uint8",
    uint16: "FieldType::Uint16",
    uint32: "FieldType::Uint32",
    uint64: "FieldType::Uint64",
    int8: "FieldType::Int8",
    int16: "FieldType::Int16",
    int32: "FieldType::Int32",
    int64: "FieldType::Int64",
    bool: "FieldType::Bool",
    enum: "FieldType::Enum",
    bitmap: "FieldType::Bitmap",
    string: "FieldType::String",
    bytes: "FieldType::Bytes",
    object: "FieldType::Object",
    array: "FieldType::Array"
  };
  return map[type] ?? "FieldType::Object";
}

function cppType(type: string): string {
  const map: Record<string, string> = {
    uint8: "std::uint8_t",
    uint16: "std::uint16_t",
    uint32: "std::uint32_t",
    uint64: "std::uint64_t",
    int8: "std::int8_t",
    int16: "std::int16_t",
    int32: "std::int32_t",
    int64: "std::int64_t",
    bool: "bool",
    string: "const char*",
    bytes: "const std::uint8_t*"
  };
  return map[type] ?? "std::uint32_t";
}

export async function emitCpp(spec: SpecModel, outDir: string): Promise<void> {
  await emitCppFiles(spec, path.join(outDir, "cpp"));
}

export async function emitCppFiles(spec: SpecModel, cppDir: string): Promise<void> {
  await Promise.all([
    writeTextFile(path.join(cppDir, "axtp_ids_generated.h"), emitIds(spec)),
    writeTextFile(path.join(cppDir, "axtp_schema_generated.h"), emitSchema(spec)),
    writeTextFile(path.join(cppDir, "axtp_method_registry_generated.h"), emitMethodRegistry(spec)),
    writeTextFile(path.join(cppDir, "axtp_event_registry_generated.h"), emitEventRegistry(spec)),
    writeTextFile(path.join(cppDir, "axtp_error_code_generated.h"), emitErrorRegistry(spec)),
    writeTextFile(path.join(cppDir, "axtp_capability_generated.h"), emitCapabilityRegistry(spec)),
    writeTextFile(path.join(cppDir, "axtp_legacy_mapping_generated.h"), emitLegacyMapping(spec)),
    writeTextFile(path.join(cppDir, "axtp_tlv_codec_generated.h"), emitTlvSkeleton(spec))
  ]);
}

function emitIds(spec: SpecModel): string {
  return `${banner}#pragma once
#include <cstdint>

namespace axtp {

${enumBlock("PayloadType", "std::uint8_t", spec.payloadTypes)}

${enumBlock("ControlOpcode", "std::uint8_t", spec.controlOpcodes)}

${enumBlock("RpcEncoding", "std::uint8_t", spec.rpcEncodings)}

${enumBlock("RpcBodyEncoding", "std::uint8_t", spec.rpcBodyEncodings)}

${enumBlock("RpcOp", "std::uint8_t", spec.rpcOps)}

${enumBlock("MethodId", "std::uint16_t", spec.methods)}

${enumBlock("EventId", "std::uint16_t", spec.events)}

${enumBlock("ErrorCode", "std::uint16_t", spec.errors)}

${enumBlock("CapabilityId", "std::uint16_t", spec.capabilities)}

} // namespace axtp
`;
}

function emitSchema(spec: SpecModel): string {
  const fieldArrays = spec.schemas.map((schema) => {
    const rows = schema.fields.map((field) =>
      `    { ${hex(field.id, 2)}, "${field.name}", ${fieldType(field.type)}, ${field.required}, ${field.min ?? 0}, ${field.max ?? 0} },`
    ).join("\n");
    return `inline constexpr FieldDescriptor k${cppName(schema.name)}Fields[] = {\n${rows}\n};`;
  }).join("\n\n");

  const descriptors = spec.schemas.map((schema) =>
    `inline constexpr SchemaDescriptor k${cppName(schema.name)}Schema = { "${schema.name}", k${cppName(schema.name)}Fields, ${schema.fields.length} };`
  ).join("\n");

  return `${banner}#pragma once
#include <cstddef>
#include <cstdint>

namespace axtp {

enum class FieldType : std::uint8_t {
    Uint8,
    Uint16,
    Uint32,
    Uint64,
    Int8,
    Int16,
    Int32,
    Int64,
    Bool,
    Enum,
    Bitmap,
    String,
    Bytes,
    Object,
    Array,
};

struct FieldDescriptor {
    std::uint8_t field_id;
    const char* name;
    FieldType type;
    bool required;
    std::uint32_t min_value;
    std::uint32_t max_value;
};

struct SchemaDescriptor {
    const char* name;
    const FieldDescriptor* fields;
    std::size_t field_count;
};

${fieldArrays}

${descriptors}

} // namespace axtp
`;
}

